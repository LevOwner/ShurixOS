// Инициализация графики из Вашего ядра (kernel_main)
void init_graphics(multiboot_info_t* mbi) {
    if (mbi->flags & 0x00001000) { // Проверка бита Framebuffer Info
        fb = (uint32_t*)(uint32_t)mbi->framebuffer_addr;
        fb_width = mbi->framebuffer_width;
        fb_height = mbi->framebuffer_height;
        fb_pitch = mbi->framebuffer_pitch / 4;
    }
}

// Преобразование палитры VGA в 32-битный цвет RGB
static uint32_t vga_to_rgb(uint8_t vga_color) {
    switch (vga_color & 0x0F) {
        case 0x00: return 0x000000; // Black
        case 0x01: return 0x0000AA; // Blue
        case 0x02: return 0x00AA00; // Green
        case 0x03: return 0x00AAAA; // Cyan
        case 0x04: return 0xAA0000; // Red
        case 0x05: return 0xAA00AA; // Magenta
        case 0x06: return 0xAA5500; // Brown
        case 0x07: return 0xAAAAAA; // Light Gray
        case 0x08: return 0x555555; // Dark Gray
        case 0x09: return 0x5555FF; // Light Blue
        case 0x0A: return 0x55FF55; // Light Green
        case 0x0B: return 0x55FFFF; // Light Cyan
        case 0x0C: return 0xFF5555; // Light Red
        case 0x0D: return 0xFF55FF; // Light Magenta
        case 0x0E: return 0xFFFF55; // Yellow
        case 0x0F: return 0xFFFFFF; // White
    }
    return 0x000000;
}

void vga_clear_blue(void) {
    if (!fb) return;
    uint32_t blue = vga_to_rgb(0x01); // Базовый синий
    for (uint32_t i = 0; i < fb_pitch * fb_height; i++) {
        fb[i] = blue;
    }
    cursor_x = 0;
    cursor_y = 0;
}

void vga_putchar(char c, uint8_t color) {
    if (!fb) return;

    if (c == '\n') { 
        cursor_x = 0; 
        cursor_y++; 
        return; 
    }

    uint32_t fg = vga_to_rgb(color & 0x0F);
    uint32_t bg = vga_to_rgb((color >> 4) & 0x0F);

    int pixel_x = cursor_x * 8;
    int pixel_y = cursor_y * 16;

    // Отрисовка символа 8x16 из матрицы шрифта
for (int row = 0; row < 16; row++) {uint8_t bits = font[(uint8_t)c][row];for (int col = 0; col < 8; col++) {if (pixel_y + row < (int)fb_height && pixel_x + col < (int)fb_width) {if (bits & (0x80 >> col)) {fb[(pixel_y + row) * fb_pitch + (pixel_x + col)] = fg;} else {fb[(pixel_y + row) * fb_pitch + (pixel_x + col)] = bg;}}}}cursor_x++;if (cursor_x >= VGA_COLS) {cursor_x = 0;cursor_y++;}
}

void print_color_full(const char *s, uint8_t fg_color, uint8_t bg_color) {
    uint8_t combined = (bg_color << 4) | fg_color;
    while (*s) vga_putchar(*s++, combined);
}

void print(const char *s) {
    print_color_full(s, 0x0F, 0x01);
}

void print_color(const char *s, uint8_t color) {
    while (*s) vga_putchar(*s++, color);
}

void print_int(int n) {
    char buf[16];
    int i = 0;
    if (n == 0) { vga_putchar('0', 0x1F); return; }
    while (n > 0) {
        buf[i++] = '0' + (n % 10);
        n /= 10;
    }
    while (i--) vga_putchar(buf[i], 0x1F);
}

void draw_box(void) {
    cursor_x = 0; cursor_y = 8;
    vga_putchar(0xC9, 0x07);
    for (int i = 0; i < 78; i++) vga_putchar(0xCD, 0x07);
    vga_putchar(0xBB, 0x07);

    for (int i = 0; i < 11; i++) {
        cursor_x = 0; cursor_y = 9 + i;
        vga_putchar(0xBA, 0x07);
        for (int j = 0; j < 78; j++) vga_putchar(' ', 0x17);
        vga_putchar(0xBA, 0x07);
    }

    cursor_x = 0; cursor_y = 20;
    vga_putchar(0xC8, 0x07);
    for (int i = 0; i < 78; i++) vga_putchar(0xCD, 0x07);
    vga_putchar(0xBC, 0x07);
}

